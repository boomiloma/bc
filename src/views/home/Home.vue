        },
        cockroachPig: {
          show_options: false,
          rows: 6,
          cols: 19,
        },
      },
    };
  },

  watch: {
    config: {
      handler(val) {
        localStorage.setItem("roadmap-config", JSON.stringify(val));
      },
      deep: true,
    },
  },

  created() {
    this.initLocalConfig();
    this.roadmapUtils = new RoadmapUtilities();
    this.initRoadmap();
  },

  methods: {
    initLocalConfig() {
      const localConfig = localStorage.getItem("roadmap-config");

      if (localConfig) {
        this.config = (() => {
          try {
            return JSON.parse(localConfig);
          } catch (error) {
            return this.config;
          }
        })();
      }
    },

    clearRoadmap() {
      this.results = [];
      this.initRoadmap();
    },

    initRoadmap() {
      this.roadmap = new Roadmap({
        results: this.results,
        config: this.config,
      });
    },

    push(key) {
      let rult = {
        p: [1,2]
      }
      this.results.push(key);
      console.log(this.results, 'results');
      this.roadmap.push(key);
    },

    // eslint-disable-next-line vue/no-unused-properties
    formatArray(arr) {
      const str = JSON.stringify(arr);

      return str
        .replace(/(\[)(\[)/g, "$1\n$2")
        .replace(/(\])(\])/g, "$1\n$2")
        .replace(/(\],)(\[)/g, "$1\n$2")
        .replace(/(\[.)/g, "  $1");
    },

    saveConfig(configKey) {
      const config = this.config[configKey];

      config.show_options = false;

      this.initRoadmap();
    },

    saveManualResult() {
      const manualResultsArray = this.manualMarks.split("");

      const isValidResults = manualResultsArray.every((mark) => {
        return this.roadmapUtils.identityDictionary[mark];
      });

      if (!isValidResults) {
        return alert("Invalid marks");
      }

      this.results = manualResultsArray;
      this.initRoadmap();
    },
    onChangeLanuage(){
      this.$i18n.locale = 'en'
    }
  },
  mounted(){
    this.onChangeLanuage();
  }
};
